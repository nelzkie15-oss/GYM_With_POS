    document.addEventListener('DOMContentLoaded', () => {
        let btn = document.querySelector('#btn-addProInvStockout');
        btn.addEventListener('click', (e) => {
            e.preventDefault();


            const product = document.querySelector('input[id=product]').value;
            console.log(product);

            const quantity = document.querySelector('input[id=quantity]').value;
            console.log(quantity);

            const description = document.querySelector('input[id=description]').value;
            console.log(description);

            const expiration_date = document.querySelector('input[id=expiration_date]').value;
            console.log(expiration_date);

            const price = document.querySelector('input[id=price]').value;
            console.log(price);

            const brand = document.querySelector('input[id=brand]').value;
            console.log(brand);

            const date_ofpurchased = document.querySelector('input[id=date_ofpurchased]').value;
            console.log(date_ofpurchased);

            const reason = document.querySelector('input[id=reason]').value;
            console.log(reason);

            var data = new FormData(this.form);

            data.append('product', product);
            data.append('quantity', quantity);
            data.append('description', description);
            data.append('expiration_date', expiration_date);
            data.append('price', price);
            data.append('brand', brand);
            data.append('date_ofpurchased', date_ofpurchased);
            data.append('reason', reason);



        if (product === '' ||  quantity ==='' ||  description ==='' || expiration_date === '' || price === '' || brand === '' || date_ofpurchased === '' || reason === ''){
                $('#error_pimsg2').html('<div class="alert alert-danger alert-icon"><em class="icon ni ni-cross-circle"></em> <strong>Required All Fields!</strong></div>');
            }else{

             
                $.ajax({
                    url: 'main/config/process/productinvStockout_process.php',
                    type: "POST",
                    data: data,
                    processData: false,
                    contentType: false,
                    async: false,
                    cache: false,
                    success: function(response) {
                        $("#msg_piStockout").html(response).show().delay(1000).fadeOut(600);
                       },
                    error: function(response) {
                        console.log("Failed");
                    }
                });
            }
 
        });
    });
