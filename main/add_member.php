            console.log(contact_no);

            const address = document.querySelector('input[id=address]').value;
            console.log(address);

            const joining_date = document.querySelector('input[id=joining_date]').value;
            console.log(joining_date);

            const expiring_date = document.querySelector('input[id=expiring_date]').value;
            console.log(expiring_date);



            var data = new FormData(this.form);

            data.append('name', name);
            data.append('age', age);
            data.append('sex', sex);
            data.append('birthday', birthday);
            data.append('plan', plan);
            data.append('email_address', email_address);
            data.append('contact_no', contact_no);
            data.append('address', address);
            data.append('joining_date', joining_date);
            data.append('expiring_date', expiring_date);

        if (name === '' ||  age ==='' ||  sex ==='' ||  birthday ==='' ||  plan ==='' || email_address === '' || contact_no === '' || address === '' || joining_date === '' || expiring_date === ''){
                $('#error_msg').html('<div class="alert alert-danger alert-icon"><em class="icon ni ni-cross-circle"></em> <strong>Required All Fields!</strong></div>');
            }else{

             
                $.ajax({
                    url: 'main/config/process/member_process.php',
                    type: "POST",
                    data: data,
                    processData: false,
                    contentType: false,
                    async: false,
                    cache: false,
                    success: function(response) {
                        $("#msg_member").html(response).show().delay(1000).fadeOut(600);
                       },
                    error: function(response) {
                        console.log("Failed");
                    }
                });
            }
 
        });
    });
