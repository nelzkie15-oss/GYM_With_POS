    document.addEventListener('DOMContentLoaded', () => {
        let btn = document.querySelector('#btn-addStaff');
        btn.addEventListener('click', (e) => {
            e.preventDefault();


            const full_name = document.querySelector('input[id=full_name]').value;
            console.log(full_name);

            const sex = document.querySelector('input[id=sex]').value;
            console.log(sex);

            const age = document.querySelector('input[id=age]').value;
            console.log(age);

            const date_ofbirth = document.querySelector('input[id=date_ofbirth]').value;
            console.log(date_ofbirth);

            const email_address = document.querySelector('input[id=email_address]').value;
            console.log(email_address);

            const address = document.querySelector('input[id=address]').value;
            console.log(address);

            const contact_no = document.querySelector('input[id=contact_no]').value;
            console.log(contact_no);

            const username = document.querySelector('input[id=username]').value;
            console.log(username);

            const password = document.querySelector('input[id=password]').value;
            console.log(password);

            const confirm_password = document.querySelector('input[id=confirm_password]').value;
            console.log(confirm_password);

            const role = $('#role option:selected').val();
            console.log(role);

            var data = new FormData(this.form);

            data.append('full_name', full_name);
            data.append('sex', sex);
            data.append('age', age);
            data.append('date_ofbirth', date_ofbirth);
            data.append('email_address', email_address);
            data.append('address', address);
            data.append('contact_no', contact_no);
            data.append('username', username);
            data.append('password', password);
            data.append('role', role);
 
        if (full_name === '' ||  sex ==='' ||  age ==='' ||  date_ofbirth ==='' ||  email_address ==='' || address === '' || contact_no === '' || username === '' || password === '' || role === ''){
                $('#error_msg').html('<div class="alert alert-danger alert-icon"><em class="icon ni ni-cross-circle"></em> <strong>Required All Fields!</strong></div>');
            }else

             if (password != confirm_password) {
                $('#error_msg').html('<div class="alert alert-danger alert-icon"><em class="icon ni ni-cross-circle"></em> <strong>Passwords do not match. </strong></div>'); 
                return false;
            }else{

              $('error_msg').hide();

                $.ajax({
                    url: 'main/config/process/staff_process.php',
                    type: "POST",
                    data: data,
                    processData: false,
                    contentType: false,
                    async: false,
                    cache: false,
                    success: function(response) {
                        $("#msg_staff").html(response).show().delay(1000).fadeOut(600);
                       },
                    error: function(response) {
                        console.log("Failed");
                    }
                });
            }
 
        });
    });
