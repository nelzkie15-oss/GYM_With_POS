            const brand_name = document.querySelector('input[id=brand_name]').value;
            console.log(brand_name);

            const reason = document.querySelector('input[id=reason]').value;
            console.log(reason);

            var data = new FormData(this.form);

            data.append('equipments', equipments);
            data.append('size', size);
            data.append('date_ofpurchase', date_ofpurchase);
            data.append('color', color);
            data.append('weight', weight);
            data.append('quantity', quantity);
            data.append('brand_name', brand_name);
            data.append('reason', reason);


        if (equipments === '' ||  size ==='' ||  date_ofpurchase ==='' || color === '' || weight === '' || quantity === '' || brand_name === '' || reason === ''){
                $('#error_eimsg2').html('<div class="alert alert-danger alert-icon"><em class="icon ni ni-cross-circle"></em> <strong>Required All Fields!</strong></div>');
            }else{

             
                $.ajax({
                    url: 'main/config/process/equipmentinvStockout_process.php',
                    type: "POST",
                    data: data,
                    processData: false,
                    contentType: false,
                    async: false,
                    cache: false,
                    success: function(response) {
                        $("#msg_eiStockout").html(response).show().delay(1000).fadeOut(600);
                       },
                    error: function(response) {
                        console.log("Failed");
                    }
                });
            }
 
        });
    });
